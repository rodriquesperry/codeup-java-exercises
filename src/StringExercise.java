public class StringExercise {

    public static void main(String[] args) {

        String myString = "We don't need no education \nWe don't need no thought control. ";
        System.out.println(myString);
        System.out.println();

        String noEducation = "We don't need no education";
        System.out.println(noEducation);
        System.out.println();

        String noThoughtControl = "We don't need no thought control.";
        System.out.println(noThoughtControl);
        System.out.println();

        String checkOut = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkOut);
        System.out.println();

        String drive = "In windows, the main drive is usually C:\\";
        System.out.println(drive);
        System.out.println();

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);

    }

}
